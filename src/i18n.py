_SUPPORTED_LANGUAGES = ("de", "en", "fr", "es")


def set_language(lang: str) -> None:
    """Setzt die aktive Sprache ('de', 'en', 'fr' oder 'es')."""
    global _LANG
    _LANG = lang if lang in _SUPPORTED_LANGUAGES else "de"


def current_language() -> str:
    return _LANG


def _extract_lang_code(raw: str) -> str:
    """Reduziert eine Locale-Kennung auf den 2-Buchstaben-ISO-Code."""
    if not raw:
        return ""
    head = raw.split(":", 1)[0].split(".", 1)[0]
    head = head.replace("-", "_").split("_", 1)[0]
    head = head.strip().lower()
    if head in ("c", "posix"):
        return ""
    return head[:2] if len(head) >= 2 else ""


def _iter_locale_candidates() -> list[str]:
    """Sammelt mögliche Locale-Strings in Prioritätsreihenfolge."""
    candidates: list[str] = []
    try:
        lc = locale.getlocale()
        if lc and lc[0]:
            candidates.append(lc[0])
    except Exception:
        pass
    for var in ("LC_ALL", "LC_MESSAGES", "LANG"):
        v = os.environ.get(var, "")
        if v:
            candidates.append(v)
    language_env = os.environ.get("LANGUAGE", "")
    if language_env:
        for piece in language_env.split(":"):
            if piece:
                candidates.append(piece)
    return candidates


def detect_system_language(default: str = "en") -> str:
    """Erkennt die Systemsprache und gibt einen Code aus _SUPPORTED_LANGUAGES zurück."""
    try:
        for raw in _iter_locale_candidates():
            code = _extract_lang_code(raw)
            if code in _SUPPORTED_LANGUAGES:
                return code
    except Exception:
        pass
    return default


def ensure_language(store) -> str:
    """Stellt sicher, dass store.settings.app_language gesetzt ist und aktiviert sie.

    Beim ersten Start (leere Sprache) wird die Systemsprache erkannt,
    persistiert und aktiviert. Andernfalls bleibt die gespeicherte Wahl.
    """
    lang = getattr(store.settings, "app_language", "") or ""
    if not lang:
        lang = detect_system_language(default="en")
        store.settings.app_language = lang
        store.save()
    set_language(lang)
    return lang


def _lookup_direct(text: str, lang: str) -> str | None:
    """Direkter Wörterbuch-Lookup ohne Normalisierung. None wenn nicht gefunden."""
    if lang == "fr":
        if text in _TRANSLATIONS_FR:
            return _TRANSLATIONS_FR[text]
        if text in _TRANSLATIONS:
            return _TRANSLATIONS[text]
        return None
    if lang == "es":
        if text in _TRANSLATIONS_ES:
            return _TRANSLATIONS_ES[text]
        if text in _TRANSLATIONS:
            return _TRANSLATIONS[text]
        return None
    if lang == "en":
        return _TRANSLATIONS.get(text)
    return None


def _strip_qt_decorations(text: str) -> tuple[str, str | None, bool]:
    """Entfernt Qt-Mnemonik (&) und abschließende '...' für den Lookup.

    Rückgabe: (stripped_text, mnemonic_char_lower, has_ellipsis).
    - Erstes einzelnes '&' vor einem Nicht-Whitespace-Zeichen wird entfernt;
      die folgende Letter wird (lowercase) als mnemonic_char zurückgegeben.
    - Escapeter Ampersand '&&' bleibt erhalten und gilt nicht als Mnemonik.
    - Trailing '...' (3 ASCII-Punkte) wird abgeschnitten.
    """
    has_ellipsis = text.endswith("...")
    if has_ellipsis:
        text = text[:-3]

    mnemonic_char: str | None = None
    out: list[str] = []
    i = 0
    n = len(text)
    while i < n:
        c = text[i]
        if c == "&" and i + 1 < n:
            nxt = text[i + 1]
            if nxt == "&":
                out.append("&")
                i += 2
                continue
            if mnemonic_char is None and not nxt.isspace():
                mnemonic_char = nxt.lower()
                i += 1
                continue
        out.append(c)
        i += 1
    return "".join(out), mnemonic_char, has_ellipsis


def _reattach_decorations(translation: str, mnemonic_char: str | None, has_ellipsis: bool) -> str:
    """Heftet Mnemonik und '...' wieder an die übersetzte Zeichenkette an.

    Mnemonik wird vor dem ersten Vorkommen von mnemonic_char (case-insensitive)
    eingefügt; Fallback ist der Wortanfang.
    """
    if mnemonic_char is not None:
        idx = translation.lower().find(mnemonic_char)
        if idx >= 0:
            translation = translation[:idx] + "&" + translation[idx:]
        else:
            translation = "&" + translation
    if has_ellipsis:
        translation = translation + "..."
    return translation


def _(text: str, lang: str | None = None) -> str:
    """Gibt den übersetzten Text zurück (oder Original wenn keine Übersetzung).

    Qt-Strings mit &-Mnemonik oder abschließendem '...' werden transparent
    unterstützt: zuerst direkter Lookup, dann normalisierter Lookup mit
    anschließendem Wiederanbringen der Dekorationen.
    """
    effective = lang if lang is not None else _LANG
    if effective == "de":
        return text

    direct = _lookup_direct(text, effective)
    if direct is not None:
        return direct

    stripped, mnemonic, ellipsis = _strip_qt_decorations(text)
    if stripped != text:
        normalized_hit = _lookup_direct(stripped, effective)
        if normalized_hit is not None:
            return _reattach_decorations(normalized_hit, mnemonic, ellipsis)

    return text


def ngettext(singular: str, plural: str, n: int, lang: str | None = None) -> str:
    """Pluralisierungs-Helfer (v5.9.0).

    Gibt ``singular`` zurück wenn n == 1, sonst ``plural``.
    Beide Varianten werden über ``_()`` übersetzt.

    Beispiel::
        ngettext("{n} Nachricht", "{n} Nachrichten", count).format(n=count)
    """
    template = singular if n == 1 else plural
    return _(template, lang)


def format_date(ts: float, lang: str | None = None) -> str:
    """Gibt einen lokalisierten Datums-String für einen Unix-Timestamp zurück.

    v5.9.0 – Locale-aware Datumsformatierung.

    DE/FR/ES: DD.MM.YYYY HH:MM  →  z. B. "29.03.2026 14:30"
    EN:       MM/DD/YYYY HH:MM  →  z. B. "03/29/2026 14:30"
    """
    from datetime import datetime
    dt = datetime.fromtimestamp(ts)
    effective = (lang if lang is not None else _LANG)
    if effective == "en":
        return dt.strftime("%m/%d/%Y %H:%M")
    return dt.strftime("%d.%m.%Y %H:%M")


def translation_stats(lang: str = "en") -> dict:
    """v5.9.0 – Gibt Übersetzungsstatistiken mit Coverage-Prozent zurück."""
    tables = {"en": _TRANSLATIONS, "fr": _TRANSLATIONS_FR, "es": _TRANSLATIONS_ES}
    table = tables.get(lang, _TRANSLATIONS)
    total_de = len(_TRANSLATIONS)
    translated = len(table)
    coverage = round(translated / max(total_de, 1) * 100, 1)
    return {
        "language": lang,
        "translated": translated,
        "total_de": total_de,
        "coverage_pct": coverage,
        "coverage": f"{translated}/{total_de} ({coverage}%)",
    }
